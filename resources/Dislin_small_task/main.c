{
  disfin();
}

void dislin_draw()
{
  int i;
  float  Lx1,Lx2,Ly1,Ly2,Ly;

  for (i=1; i< POINTS_NUM; i++)
  {
    x[i] = i;
  }

  Lx1=0;Lx2=POINTS_NUM;
  Ly1=-8;Ly2=log10(2);Ly=Ly2-Ly1;
  neglog(1e-9);

  graf(Lx1,Lx2,Lx1,1000,Ly1,Ly2,Ly1,Ly/10);

  curve(x,y,POINTS_NUM);
}

int main()
{
  dislin_init();
  dislin_draw();
  dislin_deinit();

  return 0;
}
